#1
number = 3.14159
rounded_to_nearest = round(number)  # Rounds to 3
rounded_to_two_decimals = round(number, 2)  # Rounds to 3.14

print(f"Original number: {number}")
print(f"Rounded to nearest integer: {rounded_to_nearest}")
print(f"Rounded to two decimal places: {rounded_to_two_decimals}")

