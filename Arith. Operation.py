#Add
x = 5
y = 3
sum = x + y
print(f"{x} + {y} = {sum}")  # Output: 5 + 3 = 8

#Sub
difference = x - y
print(f"{x} - {y} = {difference}")  # Output: 5 - 3 = 2

#Mul
product = x * y
print(f"{x} * {y} = {product}")  # Output: 5 * 3 = 15

#Div
# Performs floating-point division by default
quotient = x / y
print(f"{x} / {y} = {quotient}")  # Output: 5 / 3 = 1.6666666666666667

# Integer division (floor division) with `//`
integer_division = x // y
print(f"{x} // {y} = {integer_division}")  # Output: 5 // 3 = 1 (truncates to integer)
